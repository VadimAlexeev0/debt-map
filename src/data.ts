export function formatBillions(v: number): string {
  if (v >= 1000) return `$${(v / 1000).toFixed(1)}T`;
  if (v >= 1) return `$${v.toFixed(1)}B`;
  return `$${(v * 1000).toFixed(0)}M`;
}

export function formatPct(v: number): string {
  return `${v.toFixed(1)}%`;
}
